* Fashion is a word that itself has many hidden aspects. People follow fashion due to various reasons depending on their life style, personality and way of expressing themselves. Some adopt being fashionable as their profession. It is observed that most of the young girls are obsessed with fashion and spent a lot of money on fashion accessories.</p>
