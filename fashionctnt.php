<p class="inden">
* Trend reports are normally based on what’s been shown on the runway. But the prevalence of a certain color or pattern doesn’t necessarily mean people are interested in it. Fashion will always be an industry for visionaries – creative people who push design ideas the mass market didn’t know they wanted until seeing them. That said, fashion shows and presentations are no longer industry only events. They’re part of the marketing mix to reach customers, and through social media the fan, editor, buyer and shopper can all contribute to the data that exposes where interest is highest.</p>
